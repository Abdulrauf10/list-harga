function sumHarga(){
  let liter = 0;
  let premium = 6450;
  let pertalite = 7650;

  for(let i = 1; i <= 20; i++){
    const countLiter = liter[i]++
    const countPremium = premium[i] + premium
    const countPertalite = pertalite[i] + pertalite 

    return countLiter, countPremium, countPertalite
  }
}